/**
 * Represents the different social media networks.
 * @enum {number}
 */
export enum SocialMediaNetwork {
  /**
   * Represents the Facebook social media network.
   */
  Facebook = 0,

  /**
   * Represents X formerly known as Twitter social media network.
   */
  X = 1,

  /**
   * Represents the Instagram social media network.
   */
  Instagram = 2,

  /**
   * Represents the LinkedIn social media network.
   */
  LinkedIn = 3,

  /**
   * Represents the WhatsApp messaging platform.
   */
  WhatsApp = 4,

  /**
   * Represents the GitHub platform for version control and collaboration.
   */
  Github = 5,

  /**
   * Represents the GitLab platform for version control and collaboration.
   */
  GitLab = 6,
}

/**
 * Represents the types of experiences.
 * @enum {string}
 */
export enum ExperienceType {
  /**
   * Represents a work experience.
   */
  Work = "Work",

  /**
   * Represents an educational experience.
   */
  Education = "Education",

  /**
   * Represents a certification experience.
   */
  Certification = "Certification",

  /**
   * Represents an internship experience, such as at the end of your education.
   */
  Internship = "Internship",
}
